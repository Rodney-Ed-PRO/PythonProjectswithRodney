# A simple band name generator that generates a band name based on user input
# Print a welcome message
print("Welcome to the Band Name Generator!")
# Ask user for the city they grew up in
city_name = input("What is the name of the city you grew up in?\n")
# Ask user for the name of their pet
pet_name = input("What is the name of your pet?\n")
# Combine city name and pet name to show band name
print("Your band name could be " + city_name + " " + pet_name + "!")
